for(i = 0; i <= 0; ++i){
    if (numero %2 == 0 ){
        alert(`El numero ${numero} es par: `)
    }else if(numero %2 == 1){
        alert(`El numero ${numero} impar:`)
    } else{
        alert('!El caracter que ingresaste no es un numero!')
    }
}